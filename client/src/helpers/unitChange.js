export const ozToMl = oz => parseFloat(oz) * 29.574;
export const mlToOz = ml => parseFloat(ml) / 29.574;
export const inToCm = inch => parseFloat(inch) * 2.54;
export const cmToIn = cm => parseFloat(cm) / 2.54;
export const lbToKg = lb => parseFloat(lb) / 2.205;
export const kgToLb = kg => parseFloat(kg) * 2.205;